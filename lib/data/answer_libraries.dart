/// 答案库来源类型
enum AnswerLibrarySource {
  preset, // 预设答案库
  imported, // 用户导入的答案库
}

/// 答案库数据模型和预设答案库
class AnswerLibrary {
  final String id;
  final String name;
  final String description;
  final List<String> answers;
  final String? author;
  final String? category;
  final AnswerLibrarySource source;

  const AnswerLibrary({
    required this.id,
    required this.name,
    required this.description,
    required this.answers,
    this.author,
    this.category,
    this.source = AnswerLibrarySource.preset,
  });
}

